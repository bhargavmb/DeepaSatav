package com.JSOUPApp.model;

public class CustomerReview {
	
	private String customerName;
	private String customerRate;
	private String customerComment;
	private String customerCommentDate;
	private String pid;
	public String getPid() {
		return pid;
	}
	public void setPid(String pid) {
		this.pid = pid;
	}
	public String getCustomerName() {
		return customerName;
	}
	public void setCustomerName(String customerName) {
		this.customerName = customerName;
	}
	public String getCustomerRate() {
		return customerRate;
	}
	public void setCustomerRate(String customerRate) {
		this.customerRate = customerRate;
	}
	public String getCustomerComment() {
		return customerComment;
	}
	public void setCustomerComment(String customerComment) {
		this.customerComment = customerComment;
	}
	public String getCustomerCommentDate() {
		return customerCommentDate;
	}
	public void setCustomerCommentDate(String customerCommentDate) {
		this.customerCommentDate = customerCommentDate;
	}
	
	

}
